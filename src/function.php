            priceSlider.oninput = function() {
                var value = parseInt(this.value); // Chuyển đổi giá trị sang số nguyên
                var displayValue; // Biến để lưu giá trị hiển thị

                if (value >= 1000) { // Nếu giá trị vượt quá 1000, đổi sang đơn vị tỷ
